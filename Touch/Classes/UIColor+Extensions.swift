import UIKit

extension UIColor {
    public convenience init(value: Int, alpha: CGFloat = 1) {
        self.init(red: CGFloat((value & 0xFF0000) >> 16) / 255,
                  green: CGFloat((value & 0xFF00) >> 8) / 255,
                  blue: CGFloat(value & 0xFF) / 255,
                  alpha: alpha)
    }
}
